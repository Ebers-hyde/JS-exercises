let sum = (arr) => { 
    let sumOfArr = 0; //Объявляем переменную суммы
    for (let i = 0; i<=arr.length-1; i++) { //Цикл, который повторяется для каждого элемента массива
        sumOfArr += arr[i]; //Просто каждый раз плюсуем элемент
    }
    return sumOfArr; //Выводим результат
}

